make_violin <- function(score_col, panel_title, auc, sd_, p_val, d_val) {
  df  <- g_an %>% mutate(score = .data[[score_col]])
  sub <- glue("AUC = {round(auc,3)} ± {round(sd_,3)}",
              "    Cohen's d = {round(d_val,2)}    {fmt_p(p_val)}")
  ggplot(df, aes(x = group, y = score, fill = group, color = group)) +
    geom_violin(trim = TRUE, alpha = 0.35, linewidth = 0.7, width = 0.75) +
    geom_sina(size = 1.2, alpha = 0.30, maxwidth = 0.45, seed = 42) +
    stat_summary(fun = median, geom = "crossbar",
