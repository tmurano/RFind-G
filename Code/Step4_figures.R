# ---- ヘルパー ----
cohens_d <- function(x, y) (mean(x) - mean(y)) / sqrt((var(x) + var(y)) / 2)
fmt_p    <- function(p) {
  if (p < 0.001) return("p < 0.001")
  if (p < 0.01)  return("p < 0.01")
  sprintf("p = %.3f", p)
}

# 5-fold CV: fold ごとに z-score 化 + 学習 + 予測 → AUC
cv_auc <- function(feat_df, label, fold_vec, k = 5) {
  aucs <- numeric(k); pooled_prob <- numeric(length(label))
  for (i in seq_len(k)) {
    te <- which(fold_vec == i); tr <- which(fold_vec != i)
    mu  <- colMeans(feat_df[tr,,drop=FALSE])
    sig <- apply(  feat_df[tr,,drop=FALSE], 2, sd)
    scl <- function(d) as.data.frame(sweep(sweep(d, 2, mu, "-"), 2, sig, "/"))
    Xtr <- scl(feat_df[tr,,drop=FALSE]); Xte <- scl(feat_df[te,,drop=FALSE])
    names(Xtr) <- names(Xte) <- names(feat_df)
    fml <- as.formula(paste("y ~", paste(names(feat_df), collapse = "+")))
    fit <- glm(fml, family = binomial(), data = cbind(y = label[tr], Xtr),
               control = glm.control(maxit = 2000))
    prob <- predict(fit, newdata = Xte, type = "response")
    pooled_prob[te] <- prob
    aucs[i] <- as.numeric(auc(roc(label[te], prob, quiet = TRUE,
                                  direction = "<", levels = c(0,1))))
  }
  list(mean = mean(aucs), sd = sd(aucs),
       roc = roc(label, pooled_prob, quiet = TRUE,
                 direction = "<", levels = c(0,1)))
}

